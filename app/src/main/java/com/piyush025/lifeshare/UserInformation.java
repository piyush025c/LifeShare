package com.piyush025.lifeshare;

public class UserInformation {

    public String name;
    public String bloodgroup;
    public String mob;
    public String dob;
    public String emailID;
    public String password;
    public String gender;
    public String type;


    public UserInformation(String name,String bloodgroup,String mob,String dob,String emailID,String password,String gender,String type)
    {
        this.name=name;
        this.bloodgroup=bloodgroup;
        this.mob=mob;
        this.dob=dob;
        this.emailID=emailID;
        this.password=password;
        this.gender=gender;
        this.type=type;

    }
}
