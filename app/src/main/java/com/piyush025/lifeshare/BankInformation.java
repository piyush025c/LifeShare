package com.piyush025.lifeshare;

public class BankInformation {

    public String name;
    public String id;
    public String mob;
    public String emailID;
    public String address;
    public String password;
    public String type;


    public BankInformation(String name,String id,String mob,String emailID,String address,String password,String type)
    {
        this.name=name;
        this.id=id;
        this.mob=mob;
        this.address=address;
        this.emailID=emailID;
        this.password=password;
        this.type=type;

    }

}
